public class StudentAttenndance {
    private String AttDate;
    private int RollNumber;
    private boolean PresentOrAbsent;


    public String getAttDate() {
        return AttDate;
    }

    public void setAttDate(String attDate) {
        AttDate = attDate;
    }

    public int getRollNumber() {
        return RollNumber;
    }

    public void setRollNumber(int rollNumber) {
        RollNumber = rollNumber;
    }

    public boolean isPresentOrAbsent() {
        return PresentOrAbsent;
    }

    public void setPresentOrAbsent(boolean presentOrAbsent) {
        PresentOrAbsent = presentOrAbsent;
    }
}
